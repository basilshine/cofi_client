export type UIMessage = keyof (typeof messages)["ru"];

export const normalizeUILanguage = (value?: string): UILanguage => {
	const language = value?.toLowerCase().split(/[-_]/)[0];
	return language === "en" || language === "es" ? language : "ru";
};

export const uiText = (language: UILanguage, key: UIMessage) =>
	messages[language][key];

export const linkedIdentityErrorText = (
	language: UILanguage,
	identity: "email" | "telegram",
	message: string,
	fallback: string,
) =>
	message.toLowerCase().includes("already linked")
		? uiText(
				language,
				identity === "email" ? "emailAlreadyLinked" : "telegramAlreadyLinked",
			)
		: message || fallback;

export const spaceMemberCountText = (count: number, language: UILanguage) => {
	if (count <= 1)
		return language === "ru"
			? "Только вы"
			: language === "es"
				? "Solo tú"
				: "Only you";
	if (language === "ru") {
		const form = new Intl.PluralRules("ru").select(count);
		return `${count} ${form === "one" ? "участник" : form === "few" ? "участника" : "участников"}`;
	}
	return `${count} ${language === "es" ? "participantes" : "members"}`;
};

export const purchaseCountText = (count: number, language: UILanguage) => {
	if (language === "en")
		return `${count} ${count === 1 ? "purchase" : "purchases"}`;
	if (language === "es")
		return `${count} ${count === 1 ? "compra" : "compras"}`;
	const form = new Intl.PluralRules("ru").select(count);
	return `${count} ${form === "one" ? "покупка" : form === "few" ? "покупки" : "покупок"}`;
};

