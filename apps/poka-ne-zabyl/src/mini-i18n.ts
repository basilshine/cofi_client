export type UIMessage = keyof (typeof messages)["ru"];

export const normalizeUILanguage = (value?: string): UILanguage => {
	const language = value?.toLowerCase().split(/[-_]/)[0];
	return language === "en" || language === "es" ? language : "ru";
};

export const uiText = (language: UILanguage, key: UIMessage) =>
	messages[language][key];

